class Setup2FAResponse(BaseSchema):
    secret: str
    qr_code_uri: str
    backup_codes: list[str]


class Verify2FARequest(BaseModel):
    totp_code: str = Field(..., min_length=6, max_length=6)


class Disable2FARequest(BaseModel):
    totp_code: str = Field(..., min_length=6, max_length=6)
    password: str
